#ifndef LCD_ILI9341_DRV_H_
#define LCD_ILI9341_DRV_H_


#define LCD_ILI9341_SPI SPI5

// CSX pin
#define LCD_ILI9341_CS_CLK				RCC_AHB1Periph_GPIOC
#define LCD_ILI9341_CS_PORT				GPIOC
#define LCD_ILI9341_CS_PIN				GPIO_Pin_2

// WRX pin
#define LCD_ILI9341_WRX_CLK				RCC_AHB1Periph_GPIOD
#define LCD_ILI9341_WRX_PORT			GPIOD
#define LCD_ILI9341_WRX_PIN				GPIO_Pin_13

// Reset pin
#define LCD_ILI9341_RST_CLK				RCC_AHB1Periph_GPIOD
#define LCD_ILI9341_RST_PORT			GPIOD
#define LCD_ILI9341_RST_PIN				GPIO_Pin_12

#define LCD_ILI9341_RST_SET				GPIO_SetBits(ILI9341_RST_PORT, ILI9341_RST_PIN)
#define LCD_ILI9341_RST_RESET			GPIO_ResetBits(ILI9341_RST_PORT, ILI9341_RST_PIN)
#define LCD_ILI9341_CS_SET				GPIO_SetBits(ILI9341_CS_PORT, ILI9341_CS_PIN)
#define LCD_ILI9341_CS_RESET			GPIO_ResetBits(ILI9341_CS_PORT, ILI9341_CS_PIN)
#define LCD_ILI9341_WRX_SET				GPIO_SetBits(ILI9341_WRX_PORT, ILI9341_WRX_PIN)
#define LCD_ILI9341_WRX_RESET			GPIO_ResetBits(ILI9341_WRX_PORT, ILI9341_WRX_PIN)

// LCD settings
#define LCD_ILI9341_WIDTH 				240
#define LCD_ILI9341_HEIGHT				320
#define LCD_ILI9341_PIXEL				76800





#endif /* INC_LCD_ILI9341_DRV_H_ */
